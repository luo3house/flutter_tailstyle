import 'package:flutter/material.dart';
import 'package:tailstyle/tailstyle.dart';

class TailColorsPage extends StatelessWidget {
  const TailColorsPage({super.key});

  @override
  Widget build(BuildContext context) {
    final colorsEntries = colors.entries;
    return ListView(
      children: [
        TailBox().py_4().as((styled) {
          return styled.Container(
            child: TailTypo().text_2xl().font_bold().Text("Colors"),
          );
        }),
        ListView.builder(
          shrinkWrap: true,
          scrollDirection: Axis.vertical,
          itemCount: colorsEntries.length,
          itemBuilder: (context, index) {
            final title = colorsEntries.elementAt(index).key, colorMap = colorsEntries.elementAt(index).value;
            return Column(crossAxisAlignment: CrossAxisAlignment.start, children: [
              TailTypo().text_lg().Text(title),
              TailBox().mb_1().SizedBox(),
              Wrap(
                children: List.from(colorMap.entries.map((e) {
                  final colorTag = e.key, color = e.value;
                  return TailBox().m(5).as((styled) {
                    return styled.Container(
                      width: 100,
                      child: Column(crossAxisAlignment: CrossAxisAlignment.start, children: [
                        TailBox().rounded_md().bg(color).Container(width: 80, height: 80),
                        TailTypo().Text(colorTag.toString()),
                        TailTypo().text_color(TailColors.gray_400).Text(color2ARGB(color)),
                      ]),
                    );
                  });
                })),
              ),
              TailBox().mb_4().SizedBox(),
            ]);
          },
        ),
      ],
    );
  }

  String color2ARGB(Color color) {
    return [
      '0x',
      color.alpha.toRadixString(16).padLeft(2, '0').toUpperCase(),
      color.red.toRadixString(16).padLeft(2, '0').toUpperCase(),
      color.green.toRadixString(16).padLeft(2, '0').toUpperCase(),
      color.blue.toRadixString(16).padLeft(2, '0').toUpperCase(),
    ].join("");
  }
}

